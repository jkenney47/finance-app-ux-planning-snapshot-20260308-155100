#!/usr/bin/env node
import fs from "node:fs";

const githubToken = process.env.GITHUB_TOKEN ?? process.env.GH_TOKEN ?? "";
const repository = process.env.GITHUB_REPOSITORY ?? "";
const eventPath = process.env.GITHUB_EVENT_PATH ?? "";
const stepSummaryPath = process.env.GITHUB_STEP_SUMMARY ?? "";

const githubApiBaseUrl = "https://api.github.com";
const triageMarker = "<!-- codex-issue-autotriage:v1 -->";

const labelDefinitions = {
  "needs-triage": {
    color: "f9d0c4",
    description: "New issue awaiting automated or manual triage.",
  },
  automation: {
    color: "1d76db",
    description: "Issue is related to automation or control-plane workflows.",
  },
  blocker: {
    color: "b60205",
    description: "Issue currently blocks autonomous delivery flow.",
  },
  autopilot: {
    color: "0e8a16",
    description: "Issue affects codex-autopilot memory/control paths.",
  },
  permissions: {
    color: "fbca04",
    description: "Issue appears rooted in filesystem or permission access.",
  },
  "codex-generated": {
    color: "5319e7",
    description: "Issue appears generated by Codex automation workflows.",
  },
  "stale-blocker": {
    color: "d93f0b",
    description:
      "Blocker issue has been stale and needs explicit status update.",
  },
  "resolved-by-automation": {
    color: "0052cc",
    description:
      "Issue was automatically closed after explicit verification signal.",
  },
};

function appendSummary(line = "") {
  if (!stepSummaryPath) {
    return;
  }
  fs.appendFileSync(stepSummaryPath, `${line}\n`, "utf8");
}

function log(message) {
  console.log(`[issue-autotriage] ${message}`);
  appendSummary(`- ${message}`);
}

function fail(message) {
  console.error(`[issue-autotriage] ${message}`);
  appendSummary(`- ERROR: ${message}`);
  process.exit(1);
}

function parseRepository(input) {
  const [owner, repo] = String(input).split("/");
  if (!owner || !repo) {
    return null;
  }
  return { owner, repo };
}

function readEventPayload() {
  if (!eventPath || !fs.existsSync(eventPath)) {
    fail("Missing GITHUB_EVENT_PATH payload.");
  }

  try {
    return JSON.parse(fs.readFileSync(eventPath, "utf8"));
  } catch (error) {
    fail(
      `Unable to parse event payload: ${error instanceof Error ? error.message : String(error)}`,
    );
  }
}

async function githubRequest(
  method,
  pathname,
  body,
  { allowFailure = false } = {},
) {
  if (!githubToken) {
    fail("Missing GITHUB_TOKEN for GitHub API calls.");
  }

  const url = `${githubApiBaseUrl}/${pathname.replace(/^\/+/, "")}`;
  const response = await fetch(url, {
    method,
    headers: {
      Accept: "application/vnd.github+json",
      Authorization: `Bearer ${githubToken}`,
      "X-GitHub-Api-Version": "2022-11-28",
      "User-Agent": "finance-app-issue-autotriage",
      "Content-Type": "application/json",
    },
    body: body ? JSON.stringify(body) : undefined,
  });

  const text = await response.text();
  const payload = text ? JSON.parse(text) : {};

  if (!response.ok) {
    if (allowFailure) {
      return { ok: false, status: response.status, payload };
    }

    throw new Error(
      `GitHub API ${method} ${pathname} failed (${response.status} ${response.statusText}): ${text.slice(0, 300)}`,
    );
  }

  return { ok: true, payload };
}

function hasAny(text, fragments) {
  return fragments.some((fragment) => text.includes(fragment));
}

function hasOperationalSections(text) {
  return ["## evidence", "## impact", "## unblock signal needed"].every(
    (section) => text.includes(section),
  );
}

function analyzeIssue(issue) {
  const title = String(issue?.title ?? "");
  const body = String(issue?.body ?? "");
  const combined = `${title}\n${body}`.toLowerCase();

  const hasMemoryPermissionError =
    combined.includes("operation not permitted") &&
    combined.includes(".agents/memory");

  const isEperm =
    hasMemoryPermissionError ||
    hasAny(combined, [
      "active_context_eperm",
      "events_ndjson_eperm",
      "control-plane::autopilot_start::active_context_eperm",
      "control-plane::autopilot_run_wrapper::events_ndjson_eperm",
    ]);

  const openedByOwner =
    String(issue?.author_association ?? "").toUpperCase() === "OWNER";
  const hasCodexSignals = hasAny(combined, [
    "codex-autopilot",
    "control-plane::",
    "gatekeeper blocker",
    "[created-by: codex]",
    "created by codex",
  ]);

  const isCodexGenerated =
    hasCodexSignals ||
    isEperm ||
    (openedByOwner && hasOperationalSections(combined));

  const isAutomation =
    isCodexGenerated ||
    hasAny(combined, [
      "github actions",
      "workflow",
      "automation",
      "auto-heal",
      "gatekeeper",
    ]);

  const isBlocker =
    isEperm ||
    hasAny(combined, [
      "blocker",
      "hard blocker",
      "unblock signal needed",
      "cannot proceed",
      "fails to start",
    ]);

  const labels = new Set(["needs-triage"]);
  if (isAutomation) {
    labels.add("automation");
  }
  if (isCodexGenerated) {
    labels.add("codex-generated");
  }
  if (isBlocker) {
    labels.add("blocker");
  }
  if (isEperm) {
    labels.add("autopilot");
    labels.add("permissions");
  }

  return {
    isEperm,
    isCodexGenerated,
    labels: Array.from(labels),
  };
}

function buildTriageComment(analysis) {
  if (analysis.isEperm) {
    return `${triageMarker}
### Auto-triage: Autopilot Permission Blocker

This issue matched known autopilot filesystem permission fingerprints and was auto-labeled as a blocker.

#### Recovery runbook
1. Validate ownership and permissions:
   - \`ls -ld .agents/memory .agents/memory/active_context.md .agents/memory/events.ndjson\`
2. Restore ownership/writes if needed:
   - \`chown -R "$(whoami)" .agents/memory\`
   - \`chmod u+rw .agents/memory/active_context.md .agents/memory/events.ndjson\`
3. Verify autopilot paths:
   - \`bash .agents/hooks/codex-autopilot.sh start "permission verification"\`
   - \`bash .agents/hooks/codex-autopilot.sh run "permission verification" -- echo ok\`
4. Comment **"verification passed"** once confirmed so automation can close this issue.

If repo memory remains blocked, autopilot now falls back to a temp memory directory and should continue in degraded mode.`;
  }

  if (analysis.isCodexGenerated) {
    return `${triageMarker}
### Auto-triage: Codex Operational Issue

This issue was detected as a Codex-generated operational issue and was automatically triaged with routing labels.

Next step:
- Automation will classify this issue type and either run deterministic verification checks or delegate remediation automatically.
- When class verification passes, automation auto-closes the issue.
- If you verify manually first, comment \`verification passed\` for immediate closure.
- If still blocked, post the exact blocker still preventing closure.`;
  }

  return "";
}

async function ensureLabelsExist(owner, repo, labels) {
  for (const name of labels) {
    const definition = labelDefinitions[name];
    if (!definition) {
      continue;
    }

    const existing = await githubRequest(
      "GET",
      `repos/${owner}/${repo}/labels/${encodeURIComponent(name)}`,
      undefined,
      { allowFailure: true },
    );

    if (existing.ok) {
      continue;
    }

    if (existing.status !== 404) {
      log(
        `Skipping label creation for '${name}' due to API status ${existing.status}.`,
      );
      continue;
    }

    await githubRequest("POST", `repos/${owner}/${repo}/labels`, {
      name,
      color: definition.color,
      description: definition.description,
    });
    log(`Created missing label '${name}'.`);
  }
}

async function listIssueComments(owner, repo, issueNumber) {
  const comments = [];
  let page = 1;

  while (true) {
    const response = await githubRequest(
      "GET",
      `repos/${owner}/${repo}/issues/${issueNumber}/comments?per_page=100&page=${page}`,
    );

    const pageComments = response.payload;
    if (!Array.isArray(pageComments) || pageComments.length === 0) {
      break;
    }

    comments.push(...pageComments);

    if (pageComments.length < 100) {
      break;
    }

    page += 1;
  }

  return comments;
}

function unique(values) {
  return Array.from(new Set(values.filter(Boolean)));
}

async function main() {
  appendSummary("## Issue Auto-triage");

  const repoParts = parseRepository(repository);
  if (!repoParts) {
    fail(`Invalid GITHUB_REPOSITORY value '${repository}'.`);
  }

  const payload = readEventPayload();
  const action = String(payload?.action ?? "");
  const issue = payload?.issue;

  if (!issue) {
    log("No issue payload present. Skipping.");
    return;
  }

  if (issue.pull_request) {
    log(`Issue #${issue.number} is a pull request. Skipping.`);
    return;
  }

  if (!["opened", "edited", "reopened"].includes(action)) {
    log(`Action '${action}' is not triaged by this workflow. Skipping.`);
    return;
  }

  const { owner, repo } = repoParts;
  const analysis = analyzeIssue(issue);

  await ensureLabelsExist(owner, repo, analysis.labels);

  await githubRequest(
    "POST",
    `repos/${owner}/${repo}/issues/${issue.number}/labels`,
    {
      labels: analysis.labels,
    },
  );
  log(
    `Applied labels to issue #${issue.number}: ${analysis.labels.join(", ")}.`,
  );

  const assignees = [];
  const repositoryOwner = owner;
  const issueAuthor = issue?.user?.login;

  if (analysis.isCodexGenerated || analysis.isEperm) {
    assignees.push(repositoryOwner);
    assignees.push(issueAuthor);
  }

  const uniqueAssignees = unique(assignees);
  if (uniqueAssignees.length > 0) {
    const assignResult = await githubRequest(
      "POST",
      `repos/${owner}/${repo}/issues/${issue.number}/assignees`,
      { assignees: uniqueAssignees },
      { allowFailure: true },
    );

    if (assignResult.ok) {
      log(`Assigned issue #${issue.number} to: ${uniqueAssignees.join(", ")}.`);
    } else {
      log(
        `Unable to assign issue #${issue.number} (status ${assignResult.status}).`,
      );
    }
  }

  const triageComment = buildTriageComment(analysis);
  if (!triageComment) {
    log("No triage comment required for this issue.");
    return;
  }

  const comments = await listIssueComments(owner, repo, issue.number);
  const alreadyCommented = comments.some((comment) =>
    String(comment?.body ?? "").includes(triageMarker),
  );

  if (alreadyCommented) {
    log(`Triage comment already exists on issue #${issue.number}.`);
    return;
  }

  await githubRequest(
    "POST",
    `repos/${owner}/${repo}/issues/${issue.number}/comments`,
    {
      body: triageComment,
    },
  );
  log(`Posted auto-triage comment on issue #${issue.number}.`);
}

main().catch((error) => {
  fail(error instanceof Error ? error.message : String(error));
});
