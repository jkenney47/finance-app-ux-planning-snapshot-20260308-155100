#!/usr/bin/env node

import fs from "fs";
import path from "path";
import { spawnSync } from "child_process";

const OUTPUT_FILE = path.resolve(process.cwd(), "types/database.ts");

function parseEnvFile(content) {
  const map = new Map();

  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;

    const index = line.indexOf("=");
    if (index <= 0) continue;

    const key = line.slice(0, index).trim();
    const value = line
      .slice(index + 1)
      .trim()
      .replace(/^['"]|['"]$/g, "");
    if (key) {
      map.set(key, value);
    }
  }

  return map;
}

function resolveEnvValue(key) {
  if (process.env[key]) {
    return process.env[key];
  }

  const envFiles = [".env", ".env.local"];
  for (const file of envFiles) {
    const fullPath = path.resolve(process.cwd(), file);
    if (!fs.existsSync(fullPath)) continue;
    const parsed = parseEnvFile(fs.readFileSync(fullPath, "utf8"));
    if (parsed.has(key)) {
      return parsed.get(key);
    }
  }

  return undefined;
}

function projectRefFromUrl(url) {
  if (!url) return undefined;
  const host = url.replace(/^https?:\/\//i, "").split("/")[0];
  const ref = host.split(".")[0];
  return ref || undefined;
}

const accessToken = resolveEnvValue("SUPABASE_ACCESS_TOKEN");
if (!accessToken) {
  console.error(
    "[supabase:gen:types] Missing SUPABASE_ACCESS_TOKEN. Export this variable, then rerun npm run supabase:gen:types.",
  );
  process.exit(1);
}

const projectRef =
  resolveEnvValue("SUPABASE_PROJECT_REF") ??
  projectRefFromUrl(resolveEnvValue("EXPO_PUBLIC_SUPABASE_URL"));

if (!projectRef) {
  console.error(
    "[supabase:gen:types] Unable to resolve SUPABASE project ref. Set SUPABASE_PROJECT_REF or EXPO_PUBLIC_SUPABASE_URL.",
  );
  process.exit(1);
}

const commandArgs = [
  "--yes",
  "supabase@latest",
  "gen",
  "types",
  "typescript",
  "--project-id",
  projectRef,
  "--schema",
  "public",
];

const result = spawnSync("npx", commandArgs, {
  cwd: process.cwd(),
  env: {
    ...process.env,
    SUPABASE_ACCESS_TOKEN: accessToken,
  },
  encoding: "utf8",
});

if (result.status !== 0 || !result.stdout.trim()) {
  console.error("[supabase:gen:types] Failed to generate types.");
  if (result.stderr) {
    console.error(result.stderr.trim());
  }
  process.exit(result.status || 1);
}

const header =
  "// Generated by npm run supabase:gen:types\n" +
  `// Source project ref: ${projectRef}\n` +
  `// Generated at: ${new Date().toISOString()}\n\n`;

fs.writeFileSync(OUTPUT_FILE, `${header}${result.stdout}`, "utf8");
console.log(
  `[supabase:gen:types] Wrote ${path.relative(process.cwd(), OUTPUT_FILE)}`,
);
